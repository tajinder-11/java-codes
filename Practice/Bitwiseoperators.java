package Practice;

public class Bitwiseoperators {

	public static void main(String[] args) {
		
		int a = 6;
		int b = 7;
		
		int c = a | b;
		int d = a & b;
		int e = a ^ b;

		
		System.out.println("The result : " + c);
		System.out.println("The result : " + d);
		System.out.println("The result : " + e);
	}

}
